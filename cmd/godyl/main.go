package main

import (
	"errors"
	"fmt"
	"os"
	"path/filepath"
	"sync"

	"github.com/idelchi/godyl/internal/tools"
	"github.com/idelchi/godyl/internal/tools/sources"
	"github.com/idelchi/godyl/pkg/logger"
	"github.com/idelchi/godyl/pkg/pretty"
	"golang.org/x/sync/errgroup"
)

// TODO(Idelchi): Split into logger (with colors)

// Global variable for CI stamping.
var version = "unknown - unofficial & generated by unknown"

// result struct to hold the tool and any associated error.
type result struct {
	tool  *tools.Tool
	found string
	err   error
	msg   string
}

func main() {
	cfg, err := parseFlags()
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error parsing flags: %v\n", err)
		os.Exit(1)
	}

	if err := cfg.Validate(); err != nil {
		fmt.Fprintf(os.Stderr, "Error validating configuration: %v\n", err)
		os.Exit(1)
	}

	logger := logger.New(cfg.Log)

	if cfg.Detect {
		if err := cfg.Defaults.Defaults(); err != nil {
			fmt.Fprintf(os.Stderr, "Error setting defaults: %v\n", err)

			os.Exit(1)
		}

		logger.Info(PrintJSON(cfg.Defaults.Platform))

		os.Exit(0)
	}

	if err := updater(cfg); err != nil {
		fmt.Fprintf(os.Stderr, "Error updating: %v\n", err)

		os.Exit(1)
	}

	var toolsList tools.Tools

	err = toolsList.Load(cfg.Tools)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error loading tools: %v\n", err)
		os.Exit(1)
	}

	if err := cfg.Defaults.Defaults(); err != nil {
		fmt.Fprintf(os.Stderr, "Error setting defaults: %v\n", err)
		os.Exit(1)
	}

	// Create a channel to collect the results.
	resultCh := make(chan result)

	t, wt := splitTags(cfg.Tags)

	logger.Info("*** ***")
	logger.Info("godyl running with:")
	logger.Info("*** ***")
	logger.Info("platform:")
	logger.Info(PrintJSON(cfg.Defaults.Platform))
	logger.Info("*** ***")

	// Set up the errgroup and concurrency limit
	g := errgroup.Group{}
	if cfg.Parallel > 0 {
		g.SetLimit(cfg.Parallel)

		logger.Info("running with %d parallel downloads", cfg.Parallel)
	}

	// Create a WaitGroup to wait for the reading of results to finish
	var wg sync.WaitGroup
	wg.Add(1) // One goroutine will be reading from the resultCh

	// Start a separate goroutine to read from the result channel.
	go func() {
		defer wg.Done() // Signal that reading is done when this goroutine exits

		// Read results from the channel as they come in.
		for res := range resultCh {
			tool := res.tool
			err := res.err
			msg := res.msg
			found := res.found
			logger.Always(tool.Name)
			logger.Debug("configuration:")
			logger.Debug("-------")
			logger.Debug(pretty.YAMLMasked(tool))
			logger.Debug("-------")
			// Process tool results
			if err != nil {
				logger.Warn(tool.Name)
				if errors.Is(err, tools.ErrAlreadyExists) || errors.Is(err, tools.ErrDoesNotHaveTags) || errors.Is(err, tools.ErrDoesHaveTags) || errors.Is(err, tools.ErrSkipped) {
					logger.Warn("  %v", err)
				} else {
					logger.Error("  failed to install")
					logger.Error("configuration:")
					logger.Error("-------")
					logger.Error(pretty.JSONMasked(tool))
					logger.Error("-------")
					logger.Error("  %v", err)
					logger.Error("  %s", msg)
				}
			} else {
				if tool.Version != "" {
					logger.Info("  version: %s", tool.Version)
				}
				logger.Info("  picked download %q", filepath.Base(tool.Path))
				if tool.Mode == "find" {
					logger.Info("  picked file %q", found)
					logger.Info("  installed successfully at %q", filepath.Join(tool.Output, tool.Exe.Name))
					if tool.Aliases != nil {
						logger.Info("  symlinks:")
						for _, alias := range tool.Aliases {
							logger.Info("    - %q\n", filepath.Join(tool.Output, alias))
						}
					}
				} else {
					logger.Info("  extracted to %q", tool.Output)
				}
			}
		}
	}()

	// Start goroutines for each tool.
	for _, tool := range toolsList {
		g.Go(func() error {
			tool.ApplyDefaults(cfg.Defaults)

			if err := tool.Resolve(t, wt); err != nil {
				resultCh <- result{tool: &tool, err: err}

				return nil
			}

			if cfg.Dry {
				resultCh <- result{tool: &tool, err: nil}

				return nil
			}

			msg, found, err := tool.Download()
			resultCh <- result{tool: &tool, found: found, err: err, msg: msg}

			if err != nil {
				return nil
			}

			output, _, err := tool.Post.Install(sources.InstallData{})
			if err != nil {
				resultCh <- result{tool: &tool, err: err, msg: output}
			}

			return nil
		})
	}

	// Wait for all the goroutines to finish.
	if err := g.Wait(); err != nil {
		fmt.Fprintf(os.Stderr, "Error processing tools: %v\n", err)
	}

	// Close the result channel since no more results will be sent.
	close(resultCh)

	// Wait for the reader to finish processing all the results.
	wg.Wait()
}
