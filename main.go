/*
Godyl batch-downloads and installs statically compiled binaries from:

- GitHub releases
- GitLab releases
- URLs
- Go projects
*/
package main

import (
	"embed"
	"fmt"
	"os"

	"github.com/idelchi/godyl/internal/app"
)

// Global variable for CI stamping.
var version = "unknown - unofficial & generated by unknown"

//go:embed defaults.yml tools.yml internal/updater/scripts/*
var embeds embed.FS

func main() {
	// Execute the application
	if err := app.Execute(version, embeds); err != nil {
		fmt.Fprintf(os.Stderr, "%v\n", err)

		os.Exit(1)
	}
}
